void KeyExpansion(void *key)
{
  /* FIPS 197  P.27 Appendix A.1 Rcon[i/Nk] */ //又は mulを使用する
  int Rcon[10]={0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36};
  int i,temp;

  memcpy(w,key,nk*4);
  for(i=nk;i<NB*(nr+1);i++)
  {
    temp = w[i-1];
    if((i%nk) == 0)
      temp = SubWord(RotWord(temp)) ^ Rcon[(i/nk)-1];
    else if(nk > 6 && (i%nk) == 4)
      temp = SubWord(temp);
    w[i] = w[i-nk] ^ temp;
  }
}


